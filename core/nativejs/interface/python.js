function Python(file)
{
  if(file.endsWith(".py"))
  {
    execSync("jiphy " + file);
    file = replaceAt(file, file.length - 2, "j");
    file = replaceAt(file, file.length - 1, "s");
  }
  return file;
}
module.exports = Python;
